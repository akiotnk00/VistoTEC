    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int linha = jTableContas.getSelectedRow();
        if (linha != -1) {
            int op = JOptionPane.showConfirmDialog(null, "Deseja editar os dados da conta?");

            if (op == 0) {
                ContaAPagar c = lista.get(linha);

                bloqueiaCampos(true);
                jButtonLimpar.setEnabled(false);
                
                jTextFieldDescricao.setText(c.getDescricao());
                jTextFieldValor.setText("" + c.getValor());
                jDateChooserVencimento.setDate(c.getVencimento());
                if (c.getSituacao() == "Pago") {
                    jRadioButtonPago.setSelected(true);
                    jRadioButtonAPagar.setSelected(false);
                } else {
                    jRadioButtonPago.setSelected(false);
                    jRadioButtonAPagar.setSelected(true);
                }

                jTextFieldCodigoDeBarras.setText(c.getCodigodebarras());
                salvaid = c.getCodigo();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um registro da tabela!");
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

