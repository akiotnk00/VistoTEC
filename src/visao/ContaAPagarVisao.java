    private void jButtonPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisaActionPerformed
        switch (jComboBoxPesquisa.getSelectedIndex()) {
            case 0:
                if (!jTextFieldPesquisa.getText().equals("")) {
                    atualizaTabela(contaAPagarDAO.findByDesc(jTextFieldPesquisa.getText()));
                } else {
                    atualizaTabela(contaAPagarDAO.findAll());
                }
                break;
            case 1:
                if (!jTextFieldPesquisa.getText().equals("")) {
                    SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
                    Date date = null;

                    try {
                        date = formatter.parse(jTextFieldPesquisa.getText());
                        atualizaTabela(contaAPagarDAO.findByData(date));
                    } catch (ParseException e) {
                        JOptionPane.showMessageDialog(null, "O formato da data precisa ser dd/mm/yyyy!");
                    }
                } else {
                    atualizaTabela(contaAPagarDAO.findAll());
                }
                break;
            case 2:
                if (!jTextFieldPesquisa.getText().equals("")) {
                    atualizaTabela(contaAPagarDAO.findByValor(jTextFieldPesquisa.getText()));
                } else {
                    atualizaTabela(contaAPagarDAO.findAll());
                }
                break;
            default:
                if (!jTextFieldPesquisa.getText().equals("")) {
                    atualizaTabela(contaAPagarDAO.findBySit(jTextFieldPesquisa.getText()));
                } else {
                    atualizaTabela(contaAPagarDAO.findAll());
                }
                break;
        }

    }//GEN-LAST:event_jButtonPesquisaActionPerformed

