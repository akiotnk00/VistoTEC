    private void limparCampos() {
        txtNome.setText("");
        txtUsuario.setText("");
        txtSenha.setText("");

    }

    private void atualizaTabela(List<Usuario> usuarios) {
        DefaultTableModel dtm = (DefaultTableModel) tabela.getModel();
        dtm.setNumRows(0); // excluir os registros que estão na JTable
        lista = usuarios;
        if (lista != null) {
            for (Usuario u : lista) {
                dtm.addRow(new Object[]{u.getCodigo(), u.getNome(), u.getNomeusuario(), u.getNvacesso()});
            }
        }

    }

