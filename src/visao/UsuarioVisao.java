    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        int linha = tabela.getSelectedRow();
        if (linha != -1) {
            int ou = JOptionPane.showConfirmDialog(null, "Deseja editar os dados do cliente?");

            if (ou == 0) {
                Usuario u = lista.get(linha);

                txtNome.setText(u.getNome());
                txtUsuario.setText(u.getNomeusuario());
                txtSenha.setText(u.getSenha());
                jTextFieldID.setText(String.valueOf(u.getCodigo()));

                jLabel6.setText("Editar usuario");
                PainelComAbas.setSelectedIndex(1);
                PainelComAbas.setEnabledAt(0, false);
                PainelComAbas.setEnabledAt(1, true);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um registro da tabela!");
        }
    }//GEN-LAST:event_jButton9ActionPerformed

