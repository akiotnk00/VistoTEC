    public void atualizaTabela(Caixa cxrecebido) {
        saldo = cxrecebido.getValorinicial();

        DefaultTableModel dtm = (DefaultTableModel) jTableHistoricoCaixa.getModel();
        TableRowSorter tableSorter = new TableRowSorter(dtm);
        jTableHistoricoCaixa.setRowSorter(tableSorter);
        tableSorter.toggleSortOrder(0);
        
        dtm.setNumRows(0); // excluir os registros que estão na JTable
        vistorias = vistoriaDAO.findByCaixa(cxrecebido.getCodigo());
        movimentacoes = movimentacaoDAO.findByCodigoCaixa(cxrecebido.getCodigo());

        DateFormat dateDH = new SimpleDateFormat("dd/MM - HH:mm");
        NumberFormat nf = NumberFormat.getCurrencyInstance();  
        
        if (vistorias != null) {
            for (Vistoria v : vistorias) {
                if (v.getPagamento() != null) {
                    dtm.addRow(new Object[]{dateDH.format(v.getDatahora()), "Placa: " + v.getVeiculo().getPlaca(), "Vistoria", nf.format(v.getPagamento().getValorcobrado()), v.getCliente().getNome()});
                    saldo = saldo + v.getPagamento().getValorcobrado();
                }
            }
        }
        if (movimentacoes != null) {
            for (Movimentacao m : movimentacoes) {
                dtm.addRow(new Object[]{dateDH.format(m.getData()), m.getDescricao(), m.getTipo(), nf.format(m.getValor()), m.getOrigem()});
                if (m.getTipo().equals("Entrada")) {
                    saldo = saldo + m.getValor();
                    System.out.println(saldo);
                } else {
                    if (m.getTipo().equals("Saída")) {
                        if (m.getOrigem().equals("Caixa")) {
                            saldo = saldo - m.getValor();
                        }
                    }
                }

            }
        }

        jLabelSaldo.setText(nf.format(saldo));

    }

