    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTextFieldDescricao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo descrição!");
            jTextFieldDescricao.requestFocusInWindow();
        } else {
            if (jTextFieldValor.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha o campo valor!");
                jTextFieldValor.requestFocusInWindow();
            } else {

                Movimentacao mov = new Movimentacao();
                mov.setCaixa(caixarecebido);
                mov.setData(new Date());
                mov.setDescricao(jTextFieldDescricao.getText());
                mov.setTipo("" + jComboBoxTipo.getSelectedItem());
                mov.setOrigem("" + jComboBoxOrigem.getSelectedItem());
                mov.setValor(Double.parseDouble(jTextFieldValor.getText()));
                mov.setContaapagar(null);

                movimentacaoDAO.merge(mov);
                JOptionPane.showMessageDialog(null, "Movimentação registrada com sucesso!");
                limpaCampos();

                jTabbedPane1.setSelectedIndex(0);
                jTabbedPane1.setEnabledAt(1, false);
                jTabbedPane1.setEnabledAt(0, true);

                atualizaTabela(caixarecebido);
            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed

