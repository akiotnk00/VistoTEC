    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jRadioButtonAberto.isSelected()) {
            agendamentoEditado.setStatus('a');
        } else if (jRadioButtonFinalizado.isSelected()) {
            agendamentoEditado.setStatus('f');
        } else {
            agendamentoEditado.setStatus('c');
        }

        agendamentoDAO.merge(agendamentoEditado);
        JOptionPane.showMessageDialog(null, "Status do agendamento salvo!");
        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

