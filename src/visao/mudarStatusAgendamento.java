    public mudarStatusAgendamento(Agendamento agen) {
        initComponents();
        this.setIconImage(new ImageIcon(getClass().getResource("/icones/vistotec-logo.png")).getImage());
        agendamentoDAO = new AgendamentoDAO();
        DateFormat dateDia = new SimpleDateFormat("dd/MM/yyyy - HH:00");
        jTextFieldAgendamento.setText(dateDia.format(agen.getDataagendamento()));
        agendamentoEditado = agen;

        if (agen.getStatus() == 'a') {
            jRadioButtonAberto.setSelected(true);
        } else if (agen.getStatus() != 'f') {
            jRadioButtonFinalizado.setSelected(true);
        } else {
            jRadioButtonCancelado.setSelected(true);
        }
    }

